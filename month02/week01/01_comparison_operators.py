# Copmparison Operator in Python

a = 6
b = 7

# ==  -> Equal to 
print(a == b) # False

# !=  -> Not Equal To
print(a != b) # True

# >  -> Greater Than
print(a > b) # False

# >= -> Greater Than or equal to
print(a >= b) # False

# < -> Less Than
print(a < b) # True

# <= -> Less Than or equal to
print(a <= b) # True